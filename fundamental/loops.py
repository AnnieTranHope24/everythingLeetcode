n = 0
print('Test while loop')
while n < 5:
    print(n)
    n += 1

# Loop from 0 to 4
print('Test for loop with range(5)')
for i in range(5):
    print(i)

# Loop from 2 to 5
print('Test for loop with range(2, 6)')
for i in range(2, 6):
    print(i)

# Loop from 5 to 2
print('Test for loop with range(5, 1, -1)')
for i in range(5, 1, -1):
    print(i)

