# Variables are dynamically typed

n = 0
print('At first, n = ', n)

n = "abc"
print('Then, n = ', n)

# Multiple assignments
a, b, c = 1, "Annie", True
print('a = ', a)
print('b = ', b)
print('c = ', c)

# Increment
n = 1
n = n + 1
print('Initial value of n: ', n)
print('n after incrementing by 1:', n)
n += 1
print('n after incrementing by 1 using n += 1: ', n)
# n++ is a bad syntax